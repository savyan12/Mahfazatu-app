-- trigger: auto-create profile + wallet + customer on auth signup
create or replace function public.handle_new_user()
returns trigger as $$
declare
  v_user_id int;
begin
  insert into public.profiles (auth_uid, name, phone_number, user_type)
  values (
    new.id,
    coalesce(new.raw_user_meta_data ->> 'name', ''),
    coalesce(new.raw_user_meta_data ->> 'phone_number', ''),
    coalesce(new.raw_user_meta_data ->> 'user_type', 'customer')
  )
  returning user_id into v_user_id;

  insert into public.wallet (user_id) values (v_user_id);

  if coalesce(new.raw_user_meta_data ->> 'user_type', 'customer') = 'customer' then
    insert into public.customer (user_id) values (v_user_id);
  end if;

  return new;
end;
$$ language plpgsql security definer;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

-- trigger: auto-update wallet.updated_at
create or replace function public.set_updated_at()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

create trigger wallet_updated_at
  before update on public.wallet
  for each row execute function public.set_updated_at();

