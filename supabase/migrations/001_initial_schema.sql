drop table if exists public.profiles        cascade;

create table public.profiles (
    user_id      int       generated by default as identity primary key,
    auth_uid     uuid      not null unique references auth.users(id) on delete cascade,
    name         text      not null,
    phone_number text      not null,
    user_type    text      not null check (user_type in ('customer', 'merchant')),
    created_at   timestamptz not null default now()
);

create table public.customer (
    user_id  int  primary key references public.profiles(user_id) on delete cascade
);

create table public.merchant (
    user_id       int   primary key references public.profiles(user_id) on delete cascade,
    merchant_code text  not null unique,
    service_type  text  not null,
    latitude      double precision,
    longitude     double precision
);

create table public.wallet (
    wallet_id int            generated by default as identity primary key,
    user_id   int            not null unique references public.profiles(user_id) on delete cascade,
    balance   numeric(15,3)  not null default 0.000 check (balance >= 0),
    points    int            not null default 0       check (points >= 0),
    status    text           not null default 'active' check (status in ('active', 'frozen')),
    created_at timestamptz   not null default now(),
    updated_at timestamptz   not null default now()
);

create table public.branch (
    branch_id    int   generated by default as identity primary key,
    merchant_id  int   not null references public.merchant(user_id) on delete cascade,
    branch_name  text  not null,
    qr_code      text  not null unique,
    location_url text,
    latitude     double precision,
    longitude    double precision,
    is_active    boolean not null default true
);

create table public.prepaid_card (
    card_id    int            generated by default as identity primary key,
    code       text           not null unique,
    amount     numeric(15,3)  not null check (amount > 0),
    is_used    boolean        not null default false,
    used_by    int            references public.profiles(user_id) on delete set null,
    used_at    timestamptz,
    expires_at timestamptz    not null
);

create table public.transaction (
    transaction_id     int            generated by default as identity primary key,
    sender_wallet_id   int            not null references public.wallet(wallet_id),
    receiver_wallet_id int            not null references public.wallet(wallet_id),
    transaction_type   text           not null check (transaction_type in ('transfer', 'payment')),
    amount             numeric(15,3)  not null check (amount > 0),
    payment_method     text           not null check (payment_method in ('NFC', 'QR', 'direct')),
    status             text           not null default 'pending' check (status in ('pending', 'completed', 'failed')),
    branch_id          int            references public.branch(branch_id) on delete set null,
    notes              text,
    transaction_date   timestamptz    not null default now(),
    check (sender_wallet_id <> receiver_wallet_id)
);

create table public.reward (
    reward_id       int   generated by default as identity primary key,
    merchant_id     int   not null references public.merchant(user_id) on delete cascade,
    reward_type     text  not null check (reward_type in ('discount', 'prize')),
    required_points int   not null check (required_points > 0),
    description     text,
    discount_text   text,
    is_active       boolean not null default true
);

create table public.user_reward (
    user_reward_id int          generated by default as identity primary key,
    user_id        int          not null references public.profiles(user_id) on delete cascade,
    reward_id      int          not null references public.reward(reward_id) on delete cascade,
    redeemed_at    timestamptz  not null default now(),
    points_spent   int          not null check (points_spent > 0),
    status         text         not null default 'pending' check (status in ('pending', 'redeemed', 'cancelled'))
);

create table public.notification (
    notification_id int          generated by default as identity primary key,
    user_id         int          not null references public.profiles(user_id) on delete cascade,
    message         text         not null,
    notif_type      text         not null default 'general' check (notif_type in ('transaction', 'reward', 'system', 'general')),
    is_read         boolean      not null default false,
    sent_at         timestamptz  not null default now()
);

