create policy "profile: own" on public.profiles
  for all using (auth.uid() = auth_uid);
create policy "customer: own read" on public.customer
  for select using (auth.uid() = (select auth_uid from public.profiles where user_id = customer.user_id));
create policy "merchant: public read" on public.merchant
  for select using (true);
create policy "wallet: own read" on public.wallet
  for select using (auth.uid() = (select auth_uid from public.profiles where user_id = wallet.user_id));
create policy "branch: public read" on public.branch
  for select using (true);
create policy "transaction: participant" on public.transaction
  for select using (
    auth.uid() in (
      select p.auth_uid from public.wallet w
        join public.profiles p on p.user_id = w.user_id
      where w.wallet_id in (sender_wallet_id, receiver_wallet_id)
    )
  );
create policy "reward: public read" on public.reward
  for select using (true);
create policy "user_reward: own" on public.user_reward
  for all using (auth.uid() = (select auth_uid from public.profiles where user_id = user_reward.user_id));
create policy "notification: own" on public.notification
  for all using (auth.uid() = (select auth_uid from public.profiles where user_id = notification.user_id));
